    private void initTable() {
        model = (DefaultTableModel) tblDSSP.getModel() ;
        model.setColumnIdentifiers(new Object[] {
            "MÃ","TÊN SẢN PHẨM","SỐ LƯỢNG","GIÁ","KÍCH THƯỚC"
        });
        model.setRowCount(0);
        tblDSSP.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblDSSP.getColumnModel().getColumn(1).setPreferredWidth(200);
        tblDSSP.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblDSSP.getColumnModel().getColumn(3).setPreferredWidth(100);
        tblDSSP.getColumnModel().getColumn(4).setPreferredWidth(80);
    }

    private void fillTable() {
        Dao_SanPham dao = new Dao_SanPham() ;
        try {
            lsp = dao.getListSP() ;
        } catch (Exception ex) {
            Logger.getLogger(GDDanhSachSP.class.getName()).log(Level.SEVERE, null, ex);
        }
        model.setRowCount(0);
        for(SanPham sp : lsp) {
            Object[] row = new Object[] {
                sp.getMasp() , sp.getTensp() , sp.getSlTon(), sp.getGia() , sp.getKichThuoc()
            } ;
            model.addRow(row);
        }
    }
    private  void fillSeclectionTable(List<SanPham> lsp) {
        model.setRowCount(0);
        for(SanPham sp: lsp) {
            Object[] row = new Object[]{
              sp.getMasp() , sp.getTensp() , sp.getSlTon() , sp.getGia() , sp.getKichThuoc()
            };
            model.addRow(row);
        }
    }
