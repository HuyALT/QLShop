        if (dig == JOptionPane.YES_OPTION) {
            String maSP = model.getValueAt(tblDSSP.getSelectedRow(), 0).toString() ;
            Dao_SanPham dao = new Dao_SanPham() ;
            try {
                dao.XoaSanPham(maSP) ;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi SQL DAO_SANPHAM", "SQL ERROR", JOptionPane.ERROR_MESSAGE);
            }
            fillTable();
        }
