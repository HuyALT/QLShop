        if (dig == JOptionPane.YES_OPTION) {
            String maNV = model.getValueAt(tblDSNV.getSelectedRow(), 0).toString() ;
            Dao_NhanVien dao = new Dao_NhanVien();
            try {
                dao.XaThaiNV(maNV) ;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi SQL DAO_NHANVIEN", "SQL ERROR", JOptionPane.ERROR_MESSAGE);
            }   
            fillTable();
        }
