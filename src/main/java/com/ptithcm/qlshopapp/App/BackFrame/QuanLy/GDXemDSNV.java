    private void initTable() {
       model = (DefaultTableModel) tblDSNV.getModel() ;
       model.setColumnIdentifiers(new Object[]{
           "MANV","TÊN","GIỚI TÍNH","CHỨC VỤ"
       });
       model.setRowCount(0);
       tblDSNV.getColumnModel().getColumn(0).setPreferredWidth(50);
       tblDSNV.getColumnModel().getColumn(1).setPreferredWidth(180);
       tblDSNV.getColumnModel().getColumn(2).setPreferredWidth(70);
       tblDSNV.getColumnModel().getColumn(3).setPreferredWidth(100);
    }

    private void fillTable() {
        Dao_NhanVien dao = new Dao_NhanVien() ;
        try {
            lnv = dao.getNVonline() ;
        } catch (Exception ex) {
            Logger.getLogger(GDXemDSNV.class.getName()).log(Level.SEVERE, null, ex);
        }
       model.setRowCount(0);
       for(NhanVien nv : lnv) {
           if (nv.getChucVu().compareTo("QUẢN LÝ") != 0) {
               Object[] row = new Object[]{
               nv.getMaNV() , nv.getTenNV() , nv.getGioiTinh() , nv.getChucVu()
               };
               model.addRow(row);    
           }
       }
    }
